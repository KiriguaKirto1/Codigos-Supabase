-- ============================================================
-- 6. TRIGGERS DE UPDATED_AT
-- ============================================================

do $$
declare
  tabela text;
begin
  foreach tabela in array array[
    'paises',
    'idiomas',
    'pais_idiomas',
    'localidades',
    'empresas',
    'modelos_ia',
    'implantacoes_ia',
    'implantacao_modelos',
    'implantacao_empresas',
    'categorias_uso',
    'implantacao_categorias_uso',
    'metricas_uso',
    'tipos_resfriamento',
    'fontes_energia',
    'infraestrutura',
    'infraestrutura_fontes_energia',
    'ambiental',
    'custos'
  ] loop
    execute format('drop trigger if exists trg_set_updated_at on public.%I', tabela);
    execute format(
      'create trigger trg_set_updated_at before update on public.%I for each row execute function public.set_updated_at()',
      tabela
    );
  end loop;
end $$;

