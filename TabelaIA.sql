-- 10. RLS / SEGURANÇA SUPABASE
-- Regra aplicada:
-- - anon e authenticated podem ler;
-- - apenas authenticated pode inserir, atualizar e deletar;
-- - created_by fica preparado para controle por usuário.
-- ============================================================

do $$
declare
  tabela text;
begin
  foreach tabela in array array[
    'paises',
    'idiomas',
    'pais_idiomas',
    'localidades',
    'empresas',
    'modelos_ia',
    'implantacoes_ia',
    'implantacao_modelos',
    'implantacao_empresas',
    'categorias_uso',
    'implantacao_categorias_uso',
    'metricas_uso',
    'tipos_resfriamento',
    'fontes_energia',
    'infraestrutura',
    'infraestrutura_fontes_energia',
    'ambiental',
    'custos'
  ] loop
    execute format('alter table public.%I enable row level security', tabela);

    execute format('drop policy if exists leitura_publica on public.%I', tabela);
    execute format('drop policy if exists inserir_autenticado on public.%I', tabela);
    execute format('drop policy if exists atualizar_autenticado on public.%I', tabela);
    execute format('drop policy if exists deletar_autenticado on public.%I', tabela);

    execute format(
      'create policy leitura_publica on public.%I for select using (true)',
      tabela
    );

    execute format(
      'create policy inserir_autenticado on public.%I for insert to authenticated with check (coalesce(created_by, auth.uid()) = auth.uid())',
      tabela
    );

    execute format(
      'create policy atualizar_autenticado on public.%I for update to authenticated using (created_by is null or created_by = auth.uid()) with check (created_by is null or created_by = auth.uid())',
      tabela
    );

    execute format(
      'create policy deletar_autenticado on public.%I for delete to authenticated using (created_by is null or created_by = auth.uid())',
      tabela
    );
  end loop;
end $$;

