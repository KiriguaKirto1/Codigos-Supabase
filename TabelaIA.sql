create or replace view public.vw_dados_completos_normalizados
with (security_invoker = true)
as
select
  p.id as pais_id,
  p.codigo_pais,
  p.nome as pais,
  p.continente,
  string_agg(distinct idi.nome, ' / ' order by idi.nome) as idiomas,
  l.id as localidade_id,
  l.cidade,
  l.regiao,
  l.clima,
  l.temp_media_c,
  l.proximidade_agua,
  ia.id as implantacao_id,
  ia.ano_instalacao,
  ia.status,
  ia.observacao,
  string_agg(distinct m.nome_publico, ' / ' order by m.nome_publico) as modelos_ia,
  string_agg(distinct coalesce(m.versao, 'Sem versão informada'), ' / ' order by coalesce(m.versao, 'Sem versão informada')) as versoes_modelos,
  string_agg(distinct m.tipo_modelo::text, ' / ' order by m.tipo_modelo::text) as tipos_modelos,
  string_agg(distinct e.nome, ' / ' order by e.nome) as empresas,
  string_agg(distinct cu.nome, ' / ' order by cu.nome) as usos_principais,
  mu.pico_uso,
  mu.usuarios_milhoes,
  mu.interacoes_dia_milhoes,
  mu.crescimento_anual_percentual,
  mu.latencia_media_ms as latencia_uso_ms,
  inf.tipo_data_center,
  inf.numero_servidores,
  tr_inf.nome as tipo_resfriamento_infraestrutura,
  inf.ano_instalacao as ano_instalacao_infraestrutura,
  inf.status as status_infraestrutura,
  inf.latencia_media_ms as latencia_infraestrutura_ms,
